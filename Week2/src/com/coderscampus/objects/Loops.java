package com.coderscampus.objects;

public class Loops {

    public static void main (String [] args) {

        //initialisation, termination condition, increment

        for (int i = 0; i <= 10; i++) {
            System.out.print(i + ": ");
            System.out.println(i);
        }

        System.out.println("Done!");
    }

}
